create or replace function public.handle_auth_user_created()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, username, first_name, last_name)
  values (
    new.id,
    nullif(lower(regexp_replace(split_part(new.email, '@', 1), '[^a-z0-9._]', '', 'g')), ''),
    coalesce(new.raw_user_meta_data ->> 'first_name', ''),
    coalesce(new.raw_user_meta_data ->> 'last_name', '')
  )
  on conflict (id) do nothing;

  insert into public.private_user_data (user_id, email, phone)
  values (
    new.id,
    new.email,
    nullif(new.raw_user_meta_data ->> 'phone', '')
  )
  on conflict (user_id) do update
    set email = excluded.email,
        phone = coalesce(excluded.phone, public.private_user_data.phone);

  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_auth_user_created();

create or replace function public.handle_auth_user_updated()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  update public.private_user_data
     set email = new.email,
         phone = coalesce(nullif(new.raw_user_meta_data ->> 'phone', ''), phone)
   where user_id = new.id;

  update public.profiles
     set first_name = coalesce(new.raw_user_meta_data ->> 'first_name', first_name),
         last_name = coalesce(new.raw_user_meta_data ->> 'last_name', last_name)
   where id = new.id;

  return new;
end;
$$;

drop trigger if exists on_auth_user_updated on auth.users;
create trigger on_auth_user_updated
after update on auth.users
for each row execute function public.handle_auth_user_updated();

create or replace function public.current_user_dashboard()
returns table (
  total_distance_m numeric,
  workouts_count bigint
)
language sql
security definer
set search_path = public
as $$
  select
    coalesce(sum(distance_m), 0) as total_distance_m,
    count(*) as workouts_count
  from public.workouts
  where user_id = auth.uid()
    and status = 'completed';
$$;

create or replace function public.leaderboard_distance(period text default 'weekly')
returns table (
  user_id uuid,
  display_name text,
  total_distance_m numeric,
  avg_pace_sec_per_km numeric,
  active_days bigint
)
language sql
security definer
set search_path = public
as $$
  with filtered as (
    select *
    from public.workouts
    where status = 'completed'
      and (
        period = 'monthly' and started_at >= timezone('utc', now()) - interval '30 days'
        or period <> 'monthly' and started_at >= timezone('utc', now()) - interval '7 days'
      )
  )
  select
    w.user_id,
    trim(concat(p.first_name, ' ', p.last_name)) as display_name,
    coalesce(sum(w.distance_m), 0) as total_distance_m,
    coalesce(avg(w.avg_pace_sec_per_km), 0) as avg_pace_sec_per_km,
    count(distinct date(w.started_at)) as active_days
  from filtered w
  join public.profiles p on p.id = w.user_id
  group by w.user_id, p.first_name, p.last_name
  order by total_distance_m desc, active_days desc, avg_pace_sec_per_km asc;
$$;

