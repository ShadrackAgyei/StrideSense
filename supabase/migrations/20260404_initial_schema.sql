create extension if not exists pgcrypto;

do $$
begin
  if not exists (select 1 from pg_type where typname = 'privacy_level') then
    create type public.privacy_level as enum ('public', 'friends', 'private');
  end if;
  if not exists (select 1 from pg_type where typname = 'club_role') then
    create type public.club_role as enum ('owner', 'admin', 'member');
  end if;
  if not exists (select 1 from pg_type where typname = 'challenge_type') then
    create type public.challenge_type as enum ('distance', 'count', 'time');
  end if;
  if not exists (select 1 from pg_type where typname = 'challenge_status') then
    create type public.challenge_status as enum ('upcoming', 'active', 'completed', 'cancelled');
  end if;
  if not exists (select 1 from pg_type where typname = 'workout_status') then
    create type public.workout_status as enum ('running', 'paused', 'completed', 'abandoned');
  end if;
  if not exists (select 1 from pg_type where typname = 'activity_type') then
    create type public.activity_type as enum ('run', 'walk', 'cycle', 'workout');
  end if;
  if not exists (select 1 from pg_type where typname = 'workout_source') then
    create type public.workout_source as enum ('mobile', 'healthkit', 'health_connect', 'manual', 'gps');
  end if;
  if not exists (select 1 from pg_type where typname = 'workout_event_type') then
    create type public.workout_event_type as enum ('start', 'pause', 'resume', 'complete');
  end if;
end $$;

create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

